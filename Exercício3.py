# 3) Programa em que o usuário entre com um número e o programa
#  imprima na tela seu antecessor e seu sucessor

numero = int(input("Digite um número inteiro"))
antecessor = numero - 1
sucessor = numero + 1
print(f"Antecessor de {numero} = {antecessor}")
print("Sucessor de {} = {}" .format(numero, sucessor))